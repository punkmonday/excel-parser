package com.lingrit.config;

/**
 * @author fxb
 */
public class Sheet {

  private int index;

  private int x;

  private int y;

  public Sheet(int index, int x, int y) {
    this.index = index;
    this.x = x;
    this.y = y;
  }

  public int getIndex() {
    return index;
  }


  public int getX() {
    return x;
  }


  public int getY() {
    return y;
  }
}
